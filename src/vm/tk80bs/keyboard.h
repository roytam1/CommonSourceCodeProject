class KEYBOARD : public DEVICE
{
private:
	DEVICE *d_pio_b, *d_pio_t, *d_cpu;
	int did_pio_b, did_pio_t;
	
	uint8* key_stat;
	uint8 prev_type, prev_brk, prev_kana;
	bool kana_lock;
	uint32 column, kb_type;
	
	void update_tk80();
	
public:
	KEYBOARD(VM* parent_vm, EMU* parent_emu) : DEVICE(parent_vm, parent_emu) {}
	~KEYBOARD() {}
	
	// common functions
	void initialize();
	void reset();
	void write_signal(int id, uint32 data, uint32 mask);
	uint32 intr_ack();
	uint32 read_signal(int ch) {
		return kb_type & 3;
	}
	
	// unique functions
	void set_context_pio_b(DEVICE* device, int id) {
		d_pio_b = device; did_pio_b = id;
	}
	void set_context_pio_t(DEVICE* device, int id) {
		d_pio_t = device; did_pio_t = id;
	}
	void set_context_cpu(DEVICE* device) {
		d_cpu = device;
	}
	void key_down(int code);
	void key_up(int code);
};

